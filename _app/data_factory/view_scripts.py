def getReadableLang(lang):
    if lang == "fr":
        return "French"
    elif lang == "eng":
        return "English"
    elif lang == "it":
        return "Italian"
    elif lang == "es":
        return "Spanish"
    elif lang == "cn":
        return "Chinese"
    elif lang == "jp":
        return "Japanese"
    elif lang == "ar":
        return "Arabic"
